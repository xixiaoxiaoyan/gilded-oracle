      '蛇': `蛇这张牌很复杂。它代表智慧，但也代表诱惑和欺骗。它可能在警告您：有些事情不像表面上那样。相信您的直觉——如果感觉不对，那很可能确实不对。但也要问：我是否在对自己不诚实？`,
      '棺材': `某个章节正在结束。这可能让人害怕，但棺材也意味着重生的必要条件。没有结束，就没有开始。是什么需要被放下？不是所有的结束都是失败——有时候它们是成长的必经之路。`,
      '花束': `好消息正在靠近。可能是赞美，礼物，或者是被看见的感觉。花束提醒您：您值得被欣赏，值得美好。如果最近感到被忽视，这张牌在说：好时光正在回来。`,
