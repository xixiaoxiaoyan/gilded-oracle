      '鞭子': `有些冲突需要被解决。可能是与他人，也可能是与自己的内在对话。不要逃避——面对它，才能穿越它。但鞭子也在问：您对自己是否太严苛了？有时候，最大的冲突是内在的。`,
      '鸟': `沟通的时刻到了。有什么话需要被说出口？有什么消息正在路上？鸟提醒您：对话可以打开门，也可以关上门。选择您的语言。同时也要学会倾听——不仅是他人的话语，还有您内心的声音。`,
